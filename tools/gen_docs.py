def md_examples(code, idx):
    out = []
    for titles, rows in LANGS:
        out.append(f"\n### {titles[idx]}\n")
        out.append(TABLE_HDR[code])
        out.append("|---|---|---|---|")
        for orig, ipa, chu, notes in rows:
            out.append(f"| {orig} | {ipa} | **{chu}** | {notes[idx]} |")
    return "\n".join(out) + "\n"

def write_md(code, idx, filename):
    path = os.path.join(ROOT, filename)
